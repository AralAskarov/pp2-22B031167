# There are three numeric types in Python:

# int
# float
# complex

x = 1    # int
y = 2.8  # float
z = 1j   # complex
print(x,y,z)
print(type(x))
print(type(y))
print(type(z))


# INT
# у инта нет ограниченний по размеру 
x = 1
y = 35656222554887711
z = -3255522

print(type(x))
print(type(y))
print(type(z))


# FLOAT
x = 1.10
y = 1.0
z = -35.59

print(type(x))
print(type(y))
print(type(z))

# Число с плавающей запятой также может быть научным числом с буквой «е», обозначающей степень 10.
x = 35e3
y = 12E4
z = -87.7e100

print(type(x))
print(type(y))
print(type(z))
print(x,y,z)

# COMPLEX
x = 3+5j
y = 5j
z = -5j

print(type(x))
print(type(y))
print(type(z))